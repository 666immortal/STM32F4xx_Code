/*********************************************************************************************************
* 函数名称: LEDFlicker
* 函数功能: LED闪烁函数，主要是为了测试
* 输入参数: cnt
* 输出参数: void
* 返 回 值: void
* 创建日期: 2018年01月01日
* 注    意: LEDFlicker在Proc2msTask中调用，因此，cnt为250时表示500毫秒更改一次LED状态
*********************************************************************************************************/
void BeepToogle(u16 cnt)
{
  static u8 s_iBeepSts = 1;   //蜂鸣器状态
  static u16 s_iCnt;          //计时器
  
  s_iCnt++;                   //计时器加1
  
  if(s_iCnt >= cnt)           //如果时间到了
  {
    s_iCnt = 0;               //计时器置0

    s_iBeepSts = !s_iBeepSts;					                      //蜂鸣器状态取反
    PCF8574WriteBit(PCF8574_ADDR_001, BEEP_O, s_iBeepSts);	//控制蜂鸣器
  }
}
